Uluwatu Bali, has the status of a temple heaven Sad Jagat or prop shaft of the wind of the island of Bali. Situated on top of a cliff with a height of about 97 m above sea level. The location is the establishment of the temple building, making tourists who come to visit, not only can enjoy the atmosphere of the sacred and religious, but also a beautiful view. The location is also what led to the name Uluwatu in Sanskrit which means the top of the rock
